void main(){

  // Variável
  String linguagem = "HTML";

  // Estrutura de escolha
  switch(linguagem){
    case "HTML": print("Linguagem de marcação"); break;
    case "CSS": print("Linguagem de estilização"); break;
    case "JavaScript": print("Linguagem de programação"); break;
    default: print("Linguagem não reconhecida");
  }

}